$(document).ready(function() {
 $("button#green").click(function() {
  $("body").removeClass();
  $("body").addClass("green-background");
 });

 $("button#purple").click(function() {
  $("body").removeClass();
  $("body").addClass("purple-background");
 });

 $("button#red").click(function() {
  $("body").removeClass();
  $("body").addClass("red-background");
 });

});
